#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	nb;
	int	a;
	int	i;

	i = 0;
	while ((str[i] < 14 && str[i] > 8) || str[i] == 32)
		i++;
	sign = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	nb = 0;
	a = 0;
	while (ft_isdigit(str[i]))
	{
		a = str[i] - '0';
		nb *= 10;
		nb = nb + a;
		i++;
	}
	return (nb * sign);
}	
